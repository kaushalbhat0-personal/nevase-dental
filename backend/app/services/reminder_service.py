def process_all_reminders(
    db: Session,
    *,
    now: datetime | None = None,
) -> dict[str, int]:
    """
    Process all reminder types in a single call.

    Designed to be called by a scheduled task / cron job.

    Args:
        db: Database session.
        now: Current time (for testability).

    Returns:
        Dict with counts of reminders generated per type.
    """
    if now is None:
        now = datetime.now(timezone.utc)

    results: dict[str, int] = {}

    try:
        events = generate_24_hour_reminders(db, now=now)
        results["24_hour"] = len(events)
    except Exception as exc:
        logger.error("Failed to generate 24-hour reminders: %s", exc)
        results["24_hour"] = 0

    try:
        events = generate_2_hour_reminders(db, now=now)
        results["2_hour"] = len(events)
    except Exception as exc:
        logger.error("Failed to generate 2-hour reminders: %s", exc)
        results["2_hour"] = 0

    try:
        events = generate_follow_up_reminders(db, now=now)
        results["follow_up"] = len(events)
    except Exception as exc:
        logger.error("Failed to generate follow-up reminders: %s", exc)
        results["follow_up"] = 0

    db.commit()
    logger.info("Reminder processing complete: %s", results)
    return results


def process_pending_deliveries(
    db: Session,
    *,
    limit: int = 50,
) -> int:
    """
    Process pending deliveries that haven't been sent yet.

    Designed to be called by a queue worker.

    Args:
        db: Database session.
        limit: Maximum number of pending deliveries to process.

    Returns:
        Number of deliveries processed.
    """
    pending = crud_notification.get_pending_deliveries(db, limit=limit)
    processed = 0

    for delivery in pending:
        try:
            from app.services.notification_service import retry_failed_delivery

            success = retry_failed_delivery(db, delivery.id)
            if success:
                processed += 1
        except Exception as exc:
            logger.error(
                "Failed to process pending delivery %s: %s",
                delivery.id,
                exc,
            )

    if processed > 0:
        db.commit()

    logger.info("Processed %d / %d pending deliveries", processed, len(pending))
    return processed
