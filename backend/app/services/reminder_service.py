# Default reminder timing (can be made tenant-configurable in future)
TWENTY_FOUR_HOUR_REMINDER = timedelta(hours=24)
TWO_HOUR_REMINDER = timedelta(hours=2)
FOLLOW_UP_REMINDER = timedelta(days=1)  # Day before follow-up

# Default channels for reminders
DEFAULT_REMINDER_CHANNELS = [NotificationChannel.email, NotificationChannel.sms]


# ═════════════════════════════════════════════════════════════════════════════
# Reminder Generation
