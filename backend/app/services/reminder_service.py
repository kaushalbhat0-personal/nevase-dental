def generate_follow_up_reminders(
    db: Session,
    *,
    now: datetime | None = None,
    channels: list[NotificationChannel] | None = None,
) -> list[NotificationEvent]:
    """
    Generate follow-up appointment reminders.

    Finds appointments with follow_up_date set to tomorrow and creates
    reminder notification events.

    Args:
        db: Database session.
        now: Current time (for testability). Defaults to UTC now.
        channels: Channels to dispatch through. Defaults to [email, sms].

    Returns:
        List of created notification events.
    """
    if now is None:
        now = datetime.now(timezone.utc)

    reminder_start = now + FOLLOW_UP_REMINDER - timedelta(hours=6)
    reminder_end = now + FOLLOW_UP_REMINDER + timedelta(hours=6)

    # Find completed appointments with follow_up_date in the target window
    stmt = select(Appointment).where(
        Appointment.status == AppointmentStatus.completed,
        Appointment.follow_up_date.isnot(None),
        Appointment.follow_up_date >= reminder_start,
        Appointment.follow_up_date <= reminder_end,
        Appointment.is_deleted == False,
    )

    appointments = list(db.scalars(stmt).all())
    logger.info(
        "Found %d appointments with follow-up dates in window",
        len(appointments),
    )

    events: list[NotificationEvent] = []
    for appt in appointments:
        # Check if reminder already sent for this appointment
        existing = _check_existing_reminder(
            db, appt.id, NotificationEventType.follow_up_reminder
        )
        if existing:
            continue

        payload = {
            "patient_name": None,  # Resolved at render time
            "doctor_name": None,
            "follow_up_date": appt.follow_up_date.isoformat() if appt.follow_up_date else None,
            "appointment_time": appt.appointment_time.isoformat(),
        }

        event = create_reminder_event(
            db,
            tenant_id=appt.tenant_id,
            patient_id=appt.patient_id,
            doctor_id=appt.doctor_id,
            appointment_id=appt.id,
            event_type=NotificationEventType.follow_up_reminder,
            payload=payload,
        )
        events.append(event)

        # Dispatch immediately (future: queue-based)
        dispatch_notification(
            db,
            event.id,
            channels=channels or DEFAULT_REMINDER_CHANNELS,
        )

    db.flush()
    logger.info("Generated %d follow-up reminder events", len(events))
    return events


def _generate_reminders_for_window(
    db: Session,
    *,
    reminder_start: datetime,
    reminder_end: datetime,
    event_type: NotificationEventType,
    channels: list[NotificationChannel],
) -> list[NotificationEvent]:
    """
    Generate reminder events for appointments in a time window.

    Finds scheduled appointments whose appointment_time falls within
    the reminder window and creates notification events.

    Args:
        db: Database session.
        reminder_start: Start of the reminder window.
        reminder_end: End of the reminder window.
        event_type: Type of reminder event to create.
        channels: Channels to dispatch through.

    Returns:
        List of created notification events.
    """
    stmt = select(Appointment).where(
        Appointment.status == AppointmentStatus.scheduled,
        Appointment.appointment_time >= reminder_start,
        Appointment.appointment_time <= reminder_end,
        Appointment.is_deleted == False,
    )

    appointments = list(db.scalars(stmt).all())
    logger.info(
        "Found %d appointments in reminder window [%s, %s]",
        len(appointments),
        reminder_start.isoformat(),
        reminder_end.isoformat(),
    )

    events: list[NotificationEvent] = []
    for appt in appointments:
        # Check if reminder already sent for this appointment
        existing = _check_existing_reminder(db, appt.id, event_type)
        if existing:
            continue

        payload = {
            "patient_name": None,  # Resolved at render time from branding context
            "doctor_name": None,
            "appointment_time": appt.appointment_time.isoformat(),
        }

        event = create_reminder_event(
            db,
            tenant_id=appt.tenant_id,
            patient_id=appt.patient_id,
            doctor_id=appt.doctor_id,
            appointment_id=appt.id,
            event_type=event_type,
            payload=payload,
        )
        events.append(event)

        # Dispatch immediately (future: queue-based)
        dispatch_notification(
            db,
            event.id,
            channels=channels,
        )

    db.flush()
    logger.info("Generated %d reminder events", len(events))
    return events


def _check_existing_reminder(
    db: Session,
    appointment_id: UUID,
    event_type: NotificationEventType,
) -> bool:
    """
    Check if a reminder event already exists for this appointment.

    Prevents duplicate reminders from being generated.
    """
    stmt = select(NotificationEvent).where(
        NotificationEvent.appointment_id == appointment_id,
        NotificationEvent.event_type == event_type,
        NotificationEvent.is_deleted == False,
    )
    existing = db.scalar(stmt)
    return existing is not None


